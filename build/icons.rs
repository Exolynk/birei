use std::error::Error;
use std::fs;

use super::{write_if_changed, BuildPaths};

pub fn generate_icon_names(paths: &BuildPaths) -> Result<(), Box<dyn Error>> {
    let scss = fs::read_to_string(&paths.lucide_scss)?;
    let mut icon_names = Vec::new();

    for line in scss.lines() {
        let Some(rest) = line.trim().strip_prefix(".icon-") else {
            continue;
        };
        let Some((name, _)) = rest.split_once("::before") else {
            continue;
        };
        icon_names.push(name.to_owned());
    }

    let mut module = String::from(
        "// This file is autogenerated by build.rs from deps/lucide0-577-0/lucide.scss.\n\
         // Do not edit it manually.\n\n",
    );

    module.push_str(&format!(
        "pub static ICON_NAMES: [&str; {}] = [\n",
        icon_names.len()
    ));
    for name in &icon_names {
        module.push_str(&format!("    \"{name}\",\n"));
    }
    module.push_str("];\n\n");

    for (index, name) in icon_names.iter().enumerate() {
        let const_name = rust_const_name(name);
        module.push_str(&format!("pub const {const_name}: usize = {index};\n"));
    }

    write_if_changed(&paths.icon_names_rs, module.as_bytes())?;

    Ok(())
}

fn rust_const_name(icon_name: &str) -> String {
    let mut const_name = String::new();

    for ch in icon_name.chars() {
        match ch {
            'a'..='z' | 'A'..='Z' | '0'..='9' => const_name.push(ch.to_ascii_uppercase()),
            '-' => const_name.push('_'),
            _ => {}
        }
    }

    if const_name
        .chars()
        .next()
        .is_some_and(|first| first.is_ascii_digit())
    {
        const_name.insert(0, '_');
    }

    const_name
}
